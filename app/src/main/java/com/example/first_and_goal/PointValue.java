import java.text.SimpleDateFormat;

public class PointValue {
     int xValue;
    float yValue;

    public PointValue(){}

    public PointValue(int xValue, float yValue){
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int getxValue(){
        return xValue;
    }

    public float getyValue(){
        return yValue;
    }




}
