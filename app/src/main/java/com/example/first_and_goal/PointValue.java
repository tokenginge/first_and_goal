package com.example.first_and_goal;

import java.text.SimpleDateFormat;

public class PointValue {
     long xValue;
    float yValue;

    public PointValue(){}

    public PointValue(long xValue, float yValue){
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public long getxValue(){
        return xValue;
    }

    public float getyValue(){
        return yValue;
    }




}
